-- Storage policies
CREATE POLICY "Users can upload own medical images"
  ON storage.objects FOR INSERT
  WITH CHECK (bucket_id = 'medical-images' AND auth.uid()::text = (storage.foldername(name))[1]);

CREATE POLICY "Users can view own medical images"
  ON storage.objects FOR SELECT
  USING (bucket_id = 'medical-images' AND auth.uid()::text = (storage.foldername(name))[1]);

CREATE POLICY "Users can delete own medical images"
  ON storage.objects FOR DELETE
  USING (bucket_id = 'medical-images' AND auth.uid()::text = (storage.foldername(name))[1]);
